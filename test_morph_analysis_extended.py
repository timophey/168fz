def test_dictionary_lookup():
    """Тест поиска слов в словарях через морфоанализ"""
    if not MORPH_AVAILABLE:
        print("⚠️ pymorphy3 не установлен, пропускаем тест")
        return
    
    manager = DictionaryManager(
        dictionaries_dir=Path('dictionaries/data'),
        use_morph_analysis=True
    )
    
    # Слова из разных областей в производных формах
    test_words = [
        # IT
        "программированием",
        "отлаженный",
        "зашифрованный",
        "обрабатывающий",
        
        # Общие
        "строительством",
        "исследованием",
        "обучением",
        "финансированием",
        
        # Специфические
        "возделыванием",
        "эксплуатирующий",
        "лицензирование",
        "модернизированный",
    ]
    
    print("\n" + "=" * 70)
    print("Тест поиска в словарях через морфоанализ")
    print("=" * 70)
    
    found_count = 0
    morph_count = 0
    
    for word in test_words:
        result = manager.check_word(word)
        found = bool(result['dictionaries'])
        via_morph = result['found_via_morph']
        
        if found:
            found_count += 1
            if via_morph:
                morph_count += 1
        
        status = "✓" if found else "✗"
        morph_tag = " [морфо]" if via_morph else ""
        found_word = result['found_word'] if found else "не найдено"
        
        print(f"  {status} '{word}' -> '{found_word}'{morph_tag}")
    
    print(f"\nНайдено: {found_count}/{len(test_words)}")
    print(f"Через морфоанализ: {morph_count}/{len(test_words)}")


def test_checker_integration():
    """Тест интеграции с checker.py"""
    if not MORPH_AVAILABLE:
        print("⚠️ pymorphy3 не установлен, пропускаем тест")
        return
    
    from checker import LanguageChecker
    
    checker = LanguageChecker()
    
    # Тестовые тексты с производными формами
    test_texts = [
        "Процесс шкурения поверхности требует внимания",
        "Программированием занимаются разработчики",
        "Строительством зданий занимаются подрядчики",
        "Обучением персонала занимается HR отдел",
        "Исследованием вопросов занимается комиссия",
    ]
    
    print("\n" + "=" * 70)
    print("Тест интеграции с LanguageChecker")
    print("=" * 70)
    
    for text in test_texts:
        result = checker.check_text(text)
        
        # Ищем слова, найденные через морфоанализ
        morph_words = [w for w in result['all_words'] if w.get('found_via_morph')]
        
        print(f"\nТекст: '{text}'")
        if morph_words:
            for w in morph_words:
                print(f"  ✓ '{w['word']}' -> найдено как '{w['found_word']}' (морфоанализ)")
        else:
            print("  (нет слов, найденных через морфоанализ)")


